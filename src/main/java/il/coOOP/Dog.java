package il.coOOP;

public class Dog {

	public int getPaws() {
		return paws;
	}
	public void setPaws(int paws) {
		this.paws = paws;
	}
	public int getTail() {
		return tail;
	}
	public void setTail(int tail) {
		this.tail = tail;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getBreed() {
		return breed;
	}
	public void setBreed(String breed) {
		this.breed = breed;
	}
	public int paws = 4;
	public int tail = 1;
	public String name;
	public String breed;
	
	
}
